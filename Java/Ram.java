class Ram
{
    // deklarasi atribut
    private int capacity;
    private int price;

    // constructor
    public Ram(){
    }
    
    // method set & get capacity
    public void setCapacity(int capacity) {
        this.capacity = capacity;
    }
    public int getCapacity() {
        return capacity;
    }

    // method set & get price
    public void setPrice(int price) {
        this.price = price;
    }
    public int getPrice() {
        return price;
    }
}